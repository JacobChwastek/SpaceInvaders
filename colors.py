class Color:
    @staticmethod
    def Black():
        return 0, 0, 0

    @staticmethod
    def White():
        return 255, 255, 255

    @staticmethod
    def Red():
        return 255, 0, 0

    @staticmethod
    def Green():
        return 0, 255, 0

    @staticmethod
    def Blue():
        return 0, 0, 255

    @staticmethod
    def Gainsboro():
        return 220, 220, 220
